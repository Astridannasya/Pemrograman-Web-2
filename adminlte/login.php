<?php
session_start();

// Fungsi untuk validasi login
function validate_login($username, $password) {
    // Kode untuk memeriksa username dan password di sini
    // Misalnya, periksa basis data atau file konfigurasi
    // Contoh sederhana:
    $valid_username = "user";
    $valid_password = "password";

    if ($username === $valid_username && $password === $valid_password) {
        return true;
    } else {
        return false;
    }
}

// Memeriksa apakah form login disubmit
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $username = $_POST["username"];
    $password = $_POST["password"];

    if (validate_login($username, $password)) {
        // Login berhasil, redirect atau tindakan selanjutnya
        $_SESSION["username"] = $username;
        header("Location: index.php");
        exit();
    } else {
        // Login gagal, tindakan yang sesuai (misalnya, menampilkan pesan kesalahan)
        $error = "Login gagal. Silakan coba lagi.";
    }
}
